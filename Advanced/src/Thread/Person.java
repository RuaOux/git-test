package Thread;

public class Person {
    private String name;

    public void run() {
        // 定义循环执行5次
        for(int i = 0; i < 5; i++) {
            System.out.println(this.name + "-->" + i);
        }
    }

    public Person(String name) {
        this.name = name;
    }

    public Person() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
